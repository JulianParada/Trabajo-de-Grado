CREATE TABLE PLANTA
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(255) NOT NULL,
    TIPO VARCHAR(255) NOT NULL,
    ORIGEN VARCHAR(255),
    SITIO_UNICO_OBT_ID BIGINT NOT NULL FOREIGN KEY REFERENCES SITIO(ID),
    REG_INVIMA VARCHAR(750),
    REG_INTERNACIONAL VARCHAR(750),
	ENCARGADO_RECOLECTA VARCHAR(750)
)

CREATE TABLE PLANTA_SITIO
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    PLANTA_ID BIGINT NOT NULL FOREIGN KEY REFERENCES PLANTA(ID),
    SITIO_ID BIGINT NOT NULL FOREIGN KEY REFERENCES SITIO(ID),
)

CREATE TABLE PROYECTO_PLANTA
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    PROYECTO_ID BIGINT NOT NULL FOREIGN KEY REFERENCES PROYECTO(ID),
    PLANTA_ID BIGINT NOT NULL FOREIGN KEY REFERENCES PLANTA(ID),
    FECHA_ACTUALIZACION VARCHAR (10)
)

CREATE TABLE INSTITUCION_ACTIVIDAD
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    ACTIVIDAD_ID BIGINT NOT NULL FOREIGN KEY REFERENCES ACTIVIDAD(ID),
    INSTITUCION_ID BIGINT NOT NULL FOREIGN KEY REFERENCES INSTITUCION(ID),
    FECHA_ACTUALIZACION VARCHAR (10)
)

CREATE TABLE USUARIO
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(255) NOT NULL,
    PASSWORD VARCHAR(255) NOT NULL,
    EMAIL VARCHAR(255) NOT NULL UNIQUE,
    ESTADO VARCHAR(255) NOT NULL,
    ROL VARCHAR(255) NOT NULL

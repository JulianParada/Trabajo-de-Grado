CREATE TABLE PROYECTO
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(255)						 ,
    TIPO VARCHAR(255) NOT NULL ,
    FASE_INV_TRAS VARCHAR(255),
    ETAPA VARCHAR(255),
    TAGS VARCHAR(255),
    URL_IMAGEN VARCHAR(750)
)

CREATE TABLE ACTIVIDAD
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    PROYECTO_ID BIGINT NOT NULL FOREIGN KEY REFERENCES PROYECTO(ID),
    DESCRIPCION VARCHAR(750),
    TIPO VARCHAR(255),
    OBJETIVO_POFI VARCHAR(750),     
    NUMERO VARCHAR(8) NOT NULL
)
--1234 5 67 8 910
CREATE TABLE TAREA
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    ACTIVIDAD_ID BIGINT NOT NULL FOREIGN KEY REFERENCES ACTIVIDAD(ID),
    DESCRIPCION VARCHAR(750),
    FECHA_INICIO VARCHAR (10),
    FECHA_FIN VARCHAR (10),
    PORCENTAJE_AVANCE FLOAT NOT NULL,
    DURACION_MESES FLOAT NOT NULL,
    FECHA_INICIO_REAL VARCHAR (10),
    FECHA_FINAL_REAL VARCHAR (10)
)

CREATE TABLE ENTREGABLE
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(750) NOT NULL,
    SUBPRODUCTO VARCHAR(750) ,
    TIPO VARCHAR(750) NOT NULL,
    PRODUCTO VARCHAR(750) ,
    ANO_PROYECCION INT 
)

CREATE TABLE INSTITUCION
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(255) NOT NULL,
    ACRONIMO VARCHAR(255),
    TIPO VARCHAR(255),
    TIPO_ORG VARCHAR(255),
    PRESUPUESTO FLOAT,
    URL_IMAGEN VARCHAR(750)
)

CREATE TABLE INFORME
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    PROYECTO_ID BIGINT NOT NULL FOREIGN KEY REFERENCES PROYECTO(ID) ,
    FECHA_CORTE VARCHAR (10)
)

CREATE TABLE ENTREGABLE_INFORME
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    INFORME_ID BIGINT NOT NULL FOREIGN KEY REFERENCES INFORME(ID),
    ENTREGABLE_ID BIGINT NOT NULL FOREIGN KEY REFERENCES ENTREGABLE(ID),
    PORCENTAJE_AVANCE FLOAT NOT NULL
)

CREATE TABLE INFORME_ACTIVIDAD
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    ACTIVIDAD_ID BIGINT NOT NULL FOREIGN KEY REFERENCES ACTIVIDAD(ID) ,
    INFORME_ID BIGINT NOT NULL FOREIGN KEY REFERENCES INFORME(ID),
    PORCENTAJE_AVANCE FLOAT NOT NULL
)

CREATE TABLE PERSONA
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(200) NOT NULL,
    ROL VARCHAR(255) ,
    ROL_PROGRAMA VARCHAR(255) ,
    NIVEL_ACADEMICO VARCHAR(255) ,
    CATEGORIA_INV VARCHAR(255) ,
    TIPO VARCHAR(255) ,
    FUNCIONES VARCHAR(750) ,
    FORMACION VARCHAR(750) ,
    CVLAC VARCHAR(255) ,
    GRUPO_LAC VARCHAR(255) ,
    IS_ACTIVE INT,
    INSTITUCION_ID BIGINT FOREIGN KEY REFERENCES INSTITUCION(ID),
    URL_IMAGEN VARCHAR(8000),
    GENERO VARCHAR(255)

)

CREATE TABLE PROYECTO_PERSONA
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    PERSONA_ID BIGINT NOT NULL FOREIGN KEY REFERENCES PERSONA(ID) ,
    PROYECTO_ID BIGINT NOT NULL FOREIGN KEY REFERENCES PROYECTO(ID),
    FECHA_ACTUALIZACION VARCHAR (10)
)

CREATE TABLE GRUPO_INV
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(255) NOT NULL,
    TIPO VARCHAR(255),
    CLASIFICACION_COLCIENCIAS VARCHAR(255),
    INSTITUCION_ID BIGINT NOT NULL FOREIGN KEY REFERENCES INSTITUCION(ID),
    TAGS VARCHAR(750)
)

CREATE TABLE PERSONA_GRUPO_INV
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    GRUPO_INV_ID BIGINT NOT NULL FOREIGN KEY REFERENCES GRUPO_INV(ID),
    PERSONA_ID BIGINT NOT NULL FOREIGN KEY   REFERENCES PERSONA(ID),
    FECHA_ACTUALIZACION VARCHAR (10)
)

CREATE TABLE SITIO
(
    ID BIGINT NOT NULL IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR(255) NOT NULL,
    LATITUD DECIMAL(18,10) NOT NULL,
    LONGITUD DECIMAL(18,10)  NOT NULL
)

